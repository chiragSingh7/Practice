public class Operations {
    public static void sum(int a, int b){
        System.out.println("Sum :" + (a+b));
    }

    public static void sum(double a, double b){
        System.out.println("Sum :" + (a+b));
    }

    public static void sum(int a, int b, int c){
        System.out.println("Sum :" + (a+b+c));
    }
}
